#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main() 
{
    int a;
    float b,c,d;
    scanf("%d",&a);
    scanf("%f",&b);
    scanf("%f",&c);
    scanf("%f",&d);
    
    printf("%d \n",a*a);
    printf("%.1f \n",3.14*b*b);
    printf("%.2f \n",0.5*c*d);
    return 0;

}
